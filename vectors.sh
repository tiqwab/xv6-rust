set -eu

# Generate vectors.S, the trap/interrupt entry points.
# There has to be one entry point per interrupt number
# since otherwise there's no way for trap() to discover
# the interrupt number.

echo "# generated by vectors.pl - do not edit"
echo "# handlers";
echo ".globl _alltraps";

for i in $(seq 0 255); do
    echo ".globl vector$i"
    echo "vector$i:";
    if [[ !(($i -eq 8) || (($i -ge 10) && ($i -le 14)) || ($i -eq 17)) ]]; then
        echo "  pushl \$0"
    fi
    echo "  pushl \$$i"
    echo "  jmp _alltraps"
done

echo ""
echo "# vector table"
echo ".data"
echo ".globl vectors"
echo "vectors:"

for i in $(seq 0 255); do
    echo "  .long vector$i"
done

# sample output:
#   # handlers
#   .globl alltraps
#   .globl vector0
#   vector0:
#     pushl $0
#     pushl $0
#     jmp alltraps
#   ...
#
#   # vector table
#   .data
#   .globl vectors
#   vectors:
#     .long vector0
#     .long vector1
#     .long vector2
#   ...

